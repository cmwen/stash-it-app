/// Design tokens for spacing used throughout the app.
abstract class AppSpacing {
  /// 4dp - Tight spacing
  static const double xs = 4;

  /// 8dp - Related elements
  static const double sm = 8;

  /// 16dp - Standard padding
  static const double md = 16;

  /// 24dp - Section spacing
  static const double lg = 24;

  /// 32dp - Major sections
  static const double xl = 32;

  /// 48dp - Screen margins (tablets)
  static const double xxl = 48;

  // Component-specific
  static const double cardPadding = 16;
  static const double listItemSpacing = 8;
  static const double screenPadding = 16;
  static const double readerHorizontalPadding = 24;
  static const double readerMaxWidth = 680;

  // Touch targets
  static const double minTouchTarget = 48;

  // Thumbnail sizes
  static const double thumbnailSize = 72;
  static const double thumbnailRadius = 8;
}
